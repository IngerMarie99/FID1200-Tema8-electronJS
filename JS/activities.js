const actionCategory = activities.filter(activity => {
    return activity.category === 'Action';
});

const shopCategory = activities.filter(activity => {
    return activity.category === 'Shopping';
});

const museumCategory = activities.filter(activity => {
    return activity.category === 'Museum';
});

const foodCategory = activities.filter(activity => {
    return activity.category === 'Restaurant';
});


export function getActivities() {

    const activitiesActionCard = document.getElementById("action");

    actionCategory.forEach(activity => {
        const activityCard = document.createElement('div');
        activityCard.classList.add('card_action');
        const activityCover = document.createElement('img');
        activityCover.setAttribute('src', activity.url);
        activityCard.append(activityCover);


        const activityName = document.createElement('h3');
        activityName.textContent = activity.name;
        activityCard.append(activityName);
        activitiesActionCard.append(activityCard)
    });

    const activitiesShoppingCard = document.getElementById("shopping");

    shopCategory.forEach(activity => {
        const activityCard = document.createElement('div');
        activityCard.classList.add('card_shopping');
        const activityCover = document.createElement('img');
        activityCover.setAttribute('src', activity.url);
        activityCard.append(activityCover);


        const activityName = document.createElement('h3');
        activityName.textContent = activity.name;
        activityCard.append(activityName);
        activitiesShoppingCard.append(activityCard)
    });

    const activitiesFoodCard = document.getElementById("food");

    foodCategory.forEach(activity => {
        const activityCard = document.createElement('div');
        activityCard.classList.add('card_food');
        const activityCover = document.createElement('img');
        activityCover.setAttribute('src', activity.url);
        activityCard.append(activityCover);


        const activityName = document.createElement('h3');
        activityName.textContent = activity.name;
        activityCard.append(activityName);
        activitiesFoodCard.append(activityCard)
    });

    const activitiesMuseumCard = document.getElementById("museum");

    museumCategory.forEach(activity => {
        const activityCard = document.createElement('div');
        activityCard.classList.add('card_museum');
        const activityCover = document.createElement('img');
        activityCover.setAttribute('src', activity.url);
        activityCard.append(activityCover);


        const activityName = document.createElement('h3');
        activityName.textContent = activity.name;
        activityCard.append(activityName);
        activitiesMuseumCard.append(activityCard)
    });
}



